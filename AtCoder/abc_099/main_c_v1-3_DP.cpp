int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int N; cin >> N;
    vector<int> dp(N+1, INT_MAX); dp[0]=0;
    
    for(int n=0; n<=N; ++n){
        for(int pow6=1; n+pow6<=N; pow6*=6){ dp[n+pow6]=min(dp[n+pow6], dp[n]+1); }
        for(int pow9=1; n+pow9<=N; pow9*=9){ dp[n+pow9]=min(dp[n+pow9], dp[n]+1); }
    }
    
    cout << dp[N] << endl;
    
    return 0;
}
